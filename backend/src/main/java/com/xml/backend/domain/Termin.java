package com.xml.backend.domain;

public class Termin {

	private String id;
	private Integer mesec;
	private int cena;
	 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer getMesec() {
		return mesec;
	}
	public void setMesec(Integer mesec) {
		this.mesec = mesec;
	}
	public int getCena() {
		return cena;
	}
	public void setCena(int cena) {
		this.cena = cena;
	}
	 
	 
}
